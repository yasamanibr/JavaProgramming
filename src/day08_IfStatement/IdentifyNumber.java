package day08_IfStatement;

public class IdentifyNumber {


    public static void main(String[] args) {

        int numb = -6;

        boolean isPositive = numb > 0;
        boolean isNegative = numb <0;
        boolean isZero = numb ==0;

        System.out.println(numb + " is positive number: " + isPositive);
        System.out.println(numb + " is positive number: " + isNegative);
        System.out.println(numb + " is positive number: " + isZero);





    }
}

